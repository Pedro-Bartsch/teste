soma = 0
for i in range(1, 51):
    soma += i
    print("Soma: ", soma)

# A primeira linha soma = 0 define a variável soma como 0 para que não possua nenhum valor anterior.
# A segunda linha faz o laço contar de 1 até 50
# A terceira linha faz a soma receber mais i, não vai até 51 por conta do +=
# A quarta linha escreve a declarando que o valor da soma é os números do comando for são mostrados.
 